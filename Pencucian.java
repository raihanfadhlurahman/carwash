    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=CarWash";
            Connection con = DriverManager.getConnection(url,"sa","sa");
            if(evt.getSource().equals(btnDelete)){
                try {
                    PreparedStatement pr = con.prepareStatement("SELECT * FROM Pesanan.tbPesanan WHERE NoPesanan = ?");
                    int KdPencucian = Integer.parseInt(txtKode.getText());
                    pr.setInt(1, KdPencucian);
                    ResultSet rs = pr.executeQuery();
                    rs.next();
                    String kode = rs.getString ("Kode Pencucian");
                    String jenis = rs.getString ("Jenis Pencucian");
                    String Harga = rs.getString ("Harga");
                   

                    txtKode.setText(kode);
                    txtJenis.setText(jenis);
                    txtHarga.setText(Harga);
                }
                catch(Exception ex){
                    System.out.println("Error: " + ex);
                }
            }
            if(evt.getSource().equals(btnDelete)){
                try {
                    String sql = "DELETE Pencucian.tbPencucian WHERE KdPencucian = ?";
                    PreparedStatement pr = con.prepareStatement(sql);
                    int KdPencucian = Integer.parseInt(txtKode.getText());
                    pr.setInt(1, KdPencucian);
                    int i = pr.executeUpdate();
                    if(i!=0)
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                    else
                    JOptionPane.showMessageDialog(null, "Data Gagal Dihapus");
                }
                catch(Exception ex){
                    JOptionPane.showMessageDialog(this, "Error: " + ex);
                }
            }
            con.close();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Error: " + ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

