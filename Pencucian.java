    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    
        String KodePencucian = txtKode.getText();
        String JenisPencucian = txtJenis.getText();
        String Harga = txtHarga.getText();
        try {
            ps = con.prepareStatement("update Pencucian.tbPencucian set JenisPencucian= ?, Harga= ? where KdPencucian=?");
            ps.setString(1, JenisPencucian);
            ps.setString(2, Harga);
            ps.setString(3, KodePencucian);
            int k = ps.executeUpdate();
            
            st=con.createStatement();
            rs = st.executeQuery("Select * From Pencucian.tbPencucian");
            tabMenu.setModel(DbUtils.resultSetToTableModel(rs));
            
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "data berhasil diperbarui");
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "data gagal diperbarui");
            }
            
        
        
        } catch(Exception ex){
          JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnEditActionPerformed

